package org.epam3.operate;

public class operate {
    public float addition(float fnum, float snum)
    {
        return fnum+snum;
    }

    public float subtraction(float fnum, float snum)
    {
        return fnum-snum;
    }

    public float multiplication(float fnum, float snum)
    {
        return fnum*snum;
    }

    public float division(float fnum, float snum)
    {
        return fnum/snum;
    }

    public float remainder(float fnum, float snum)
    {
        return fnum%snum;
    }
}
